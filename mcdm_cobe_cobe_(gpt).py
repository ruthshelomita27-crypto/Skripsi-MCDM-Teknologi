def topsis_detail(norm_df, criteria, weights):
    kode = norm_df['Kode'].values
    X = norm_df[criteria].values.astype(float)

    W = weights.set_index('Kriteria').loc[criteria, 'Bobot'].values

    # Matriks normalisasi terbobot
    V = X * W

    weighted_df = pd.DataFrame(V, columns=criteria)
    weighted_df.insert(0, 'Kode', kode)

    # Tabel 4: A+ dan A-
    A_plus = V.max(axis=0)
    A_minus = V.min(axis=0)

    table_4 = pd.DataFrame({
        'Solusi': ['A+', 'A-']
    })

    for i, col in enumerate(criteria):
        table_4[col] = [A_plus[i], A_minus[i]]

    # Tabel 5: jarak ke positive ideal D+
    D_plus = np.sqrt(((V - A_plus) ** 2).sum(axis=1))

    table_5 = pd.DataFrame({
        'Kode': kode,
        'D_plus': D_plus
    })

    table_5['D_plus_Rank'] = table_5['D_plus'].rank(
        ascending=True,
        method='dense'
    ).astype(int)

    # Tabel 6: jarak ke negative ideal D-
    D_minus = np.sqrt(((V - A_minus) ** 2).sum(axis=1))

    table_6 = pd.DataFrame({
        'Kode': kode,
        'D_minus': D_minus
    })

    table_6['D_minus_Rank'] = table_6['D_minus'].rank(
        ascending=False,
        method='dense'
    ).astype(int)

    # Tabel 7: proximity / closeness coefficient
    C_plus = D_minus / (D_plus + D_minus)

    table_7 = pd.DataFrame({
        'Kode': kode,
        'D_plus': D_plus,
        'D_minus': D_minus,
        'TOPSIS_Cplus': C_plus
    })

    table_7['TOPSIS_Rank'] = table_7['TOPSIS_Cplus'].rank(
        ascending=False,
        method='dense'
    ).astype(int)

    table_7 = table_7.sort_values('TOPSIS_Rank').reset_index(drop=True)

    return weighted_df, table_4, table_5, table_6, table_7

