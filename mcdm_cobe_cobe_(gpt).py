def entropy_weight(norm_df, criteria):
    X = norm_df[criteria].values.astype(float)

    P = X / X.sum(axis=0)

    m = X.shape[0]
    k = 1 / np.log(m)

    E = -k * np.sum(P * np.log(P), axis=0)
    D = 1 - E
    W = D / D.sum()

    weights = pd.DataFrame({
        'Kriteria': criteria,
        'Entropy': E,
        'Diversification': D,
        'Bobot': W
    })

    return weights

