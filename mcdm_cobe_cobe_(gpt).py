# ============================================================

def vikor_detail(norm_df, criteria, weights, v_values=[0.25, 0.50, 0.75, 1.00]):
    kode = norm_df['Kode'].values
    X = norm_df[criteria].values.astype(float)

    W = weights.set_index('Kriteria').loc[criteria, 'Bobot'].values

    f_star = X.max(axis=0)
    f_minus = X.min(axis=0)

    denom = f_star - f_minus
    denom[denom == 0] = 1e-12

    regret_matrix = W * ((f_star - X) / denom)

    S = regret_matrix.sum(axis=1)
    R = regret_matrix.max(axis=1)

    # Tabel 8: S, R, dan ranking
    table_8 = pd.DataFrame({
        'Kode': kode,
        'S': S,
        'S_Rank': pd.Series(S).rank(ascending=True, method='dense').astype(int),
        'R': R,
        'R_Rank': pd.Series(R).rank(ascending=True, method='dense').astype(int)
    })

    table_8 = table_8.sort_values('S_Rank').reset_index(drop=True)

    S_star, S_minus = S.min(), S.max()
    R_star, R_minus = R.min(), R.max()

    q_data = pd.DataFrame({'Kode': kode})

    for v in v_values:
        Q = (
            v * ((S - S_star) / (S_minus - S_star + 1e-12)) +
            (1 - v) * ((R - R_star) / (R_minus - R_star + 1e-12))
        )

        q_data[f'Q_v_{v:.2f}'] = Q
        q_data[f'Rank_v_{v:.2f}'] = pd.Series(Q).rank(
            ascending=True,
            method='dense'
        ).astype(int)

    # Tabel 9: Q untuk beberapa nilai v
    table_9 = q_data.copy()

    # Tabel utama VIKOR memakai v = 0.50
    main_col = 'Q_v_0.50'

    table_vikor_rank = table_9[['Kode', main_col, 'Rank_v_0.50']].copy()
    table_vikor_rank = table_vikor_rank.rename(columns={
        main_col: 'VIKOR_Q',
        'Rank_v_0.50': 'VIKOR_Rank'
    })
    table_vikor_rank = table_vikor_rank.sort_values('VIKOR_Rank').reset_index(drop=True)

    # Tabel 10: acceptable advantage
    table_10_rows = []

    m = len(kode)
    DQ = 1 / (m - 1)

    for v in v_values:
        q_col = f'Q_v_{v:.2f}'
        rank_col = f'Rank_v_{v:.2f}'

        temp = table_9[['Kode', q_col, rank_col]].copy()
        temp = temp.sort_values(q_col).reset_index(drop=True)

        best_alt = temp.loc[0, 'Kode']
        second_alt = temp.loc[1, 'Kode']

        q_best = temp.loc[0, q_col]
        q_second = temp.loc[1, q_col]

        q_diff = q_second - q_best
        acceptable_advantage = q_diff >= DQ

        table_10_rows.append({
            'v': v,
            'DQ': DQ,
            'Best_Alternative': best_alt,
            'Second_Alternative': second_alt,
            'Q_Best': q_best,
            'Q_Second': q_second,
            'Q_Second_minus_Q_Best': q_diff,
            'Acceptable_Advantage': acceptable_advantage
        })

    table_10 = pd.DataFrame(table_10_rows)

    return table_8, table_9, table_10, table_vikor_rank

