def winsorize_iqr(df, cols, k=1.5):
    df_w = df.copy()
    bounds = []

    for col in cols:
        q1 = df_w[col].quantile(0.25)
        q3 = df_w[col].quantile(0.75)
        iqr = q3 - q1

        lower = q1 - k * iqr
        upper = q3 + k * iqr

        before = df_w[col].copy()
        df_w[col] = df_w[col].clip(lower=lower, upper=upper)

        changed = (before != df_w[col]).sum()

        bounds.append({
            'Kriteria': col,
            'Q1': q1,
            'Q3': q3,
            'IQR': iqr,
            'Lower_Bound': lower,
            'Upper_Bound': upper,
            'Jumlah_Dicapping': changed
        })

    return df_w, pd.DataFrame(bounds)

