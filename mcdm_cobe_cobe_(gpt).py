# 5. NORMALISASI MIN-MAX BENEFIT/COST
# ============================================================

def minmax_normalization(df, criteria, benefit_criteria, cost_criteria, eps=1e-12):
    norm = pd.DataFrame()
    norm['Kode'] = df['Kode']

    for col in criteria:
        x_min = df[col].min()
        x_max = df[col].max()

        if x_max == x_min:
            norm[col] = 1.0
        else:
            if col in benefit_criteria:
                norm[col] = (df[col] - x_min) / (x_max - x_min)
            elif col in cost_criteria:
                norm[col] = (x_max - df[col]) / (x_max - x_min)
            else:
                raise ValueError(f"Kriteria {col} belum masuk benefit/cost")

        norm[col] = norm[col] + eps

    return norm

